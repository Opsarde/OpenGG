public class Vector3Float {
    public float x, y, z;
    
    /**
     * METHOD: constructor
     * PURPOSE: creates a Vector3Float object with x, y, and z parameters
     */
    public Vector3Float(float x, float y, float z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
